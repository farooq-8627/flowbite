"use client";
/**
 * core/shell/shell/views/dashboard/cards/DailyBriefingCard.tsx
 *
 * Sprint 5 — daily AI briefing card. Reads `aiBriefings` rows where
 * `scope = "daily-user"`. Renders the structured `payload`
 * (summary + highlights + actionItems) when present; falls back to
 * the legacy `summary` + `highlights[].text` shape so older rows still
 * render correctly until the migration sweeps them.
 *
 * Layout (Claude/OpenAI-style condensed insight pane):
 *   ┌───────────────────────────────────────────────┐
 *   │ ✨ AI Morning Briefing            · 5 min ago │
 *   │                                               │
 *   │ Summary headline (1-2 sentences)              │
 *   │                                               │
 *   │ • Highlight 1                                 │
 *   │ • Highlight 2                                 │
 *   │ • Highlight 3                                 │
 *   │                                               │
 *   │ [Action 1]  [Action 2]                        │
 *   │ ─────────────────────────────                 │
 *   │ Generated by claude-haiku-3-5     ↻ Refresh   │
 *   └───────────────────────────────────────────────┘
 */

import { useMutation, useQuery } from "convex/react";
import { anyApi } from "convex/server";
import { Bot, RefreshCw, Sparkles } from "lucide-react";
import Link from "next/link";
import { useState } from "react";
import { Button } from "@/components/ui/button";
import type { Id } from "@/convex/_generated/dataModel";
import { useOrgPermissions } from "@/core/shell/shared/hooks/useCurrentOrg";
import { toast } from "@/lib/toast";

interface Props {
	orgId: Id<"orgs">;
	orgSlug: string;
}

type BriefingShape = {
	_id: string;
	summary: string;
	generatedAt: number;
	model: string;
	highlights?: Array<{ type: string; text: string }>;
	payload?: {
		summary: string;
		highlights: string[];
		actionItems: Array<{ label: string; url?: string; toolCall?: string }>;
		trend?: "up" | "down" | "flat";
	};
};

export function DailyBriefingCard({ orgId }: Props) {
	const permissions = useOrgPermissions();
	const canRefresh = permissions.includes("ai.briefingRefresh");

	const briefing = useQuery(anyApi.ai.briefingsPublic.todayForUser, { orgId }) as
		| BriefingShape
		| null
		| undefined;

	const refresh = useMutation(anyApi.ai.briefingsPublic.refreshNow);
	const [refreshing, setRefreshing] = useState(false);

	async function handleRefresh() {
		setRefreshing(true);
		try {
			await refresh({ orgId });
			toast.success("Generating new briefing", "It will appear in a few seconds.");
		} catch (err) {
			toast.mutationError(err, "Could not refresh briefing.");
		} finally {
			setRefreshing(false);
		}
	}

	if (briefing === undefined) {
		return (
			<div className="flex h-32 items-center justify-center rounded-[var(--radius)] border bg-card">
				<Bot className="size-5 animate-pulse text-muted-foreground" />
			</div>
		);
	}

	if (briefing === null) {
		return (
			<div className="flex flex-col gap-3 rounded-[var(--radius)] border bg-card p-4">
				<div className="flex items-center gap-2">
					<Sparkles className="size-4 text-primary" />
					<span className="font-semibold text-sm">AI Morning Briefing</span>
				</div>
				<p className="text-sm text-muted-foreground">
					Your daily briefing hasn't been generated yet. Click below to create one.
				</p>
				{canRefresh && (
					<Button
						size="sm"
						onClick={handleRefresh}
						disabled={refreshing}
						className="self-start gap-1.5"
					>
						<RefreshCw className={`size-3.5 ${refreshing ? "animate-spin" : ""}`} />
						Generate briefing
					</Button>
				)}
			</div>
		);
	}

	// Prefer the structured payload when available; fall back to legacy
	// summary+highlights so pre-Sprint-5 rows still render.
	const summary = briefing.payload?.summary ?? briefing.summary;
	const highlights =
		briefing.payload?.highlights ??
		(briefing.highlights ?? []).map((h) => h.text).filter(Boolean);
	const actionItems = briefing.payload?.actionItems ?? [];

	const generatedMinAgo = Math.floor((Date.now() - briefing.generatedAt) / 60000);
	const generatedLabel =
		generatedMinAgo < 1
			? "just now"
			: generatedMinAgo < 60
				? `${generatedMinAgo} min ago`
				: `${Math.floor(generatedMinAgo / 60)} h ago`;

	return (
		<div className="flex flex-col gap-3 rounded-[var(--radius)] border bg-card p-4">
			<div className="flex items-center gap-2">
				<Sparkles className="size-4 text-primary" />
				<span className="flex-1 font-semibold text-sm">AI Morning Briefing</span>
				<span className="text-[10px] text-muted-foreground/60">{generatedLabel}</span>
			</div>

			<p className="text-sm leading-relaxed text-foreground whitespace-pre-wrap">{summary}</p>

			{highlights.length > 0 && (
				<ul className="flex flex-col gap-1 text-xs text-foreground/90">
					{highlights.slice(0, 5).map((h) => (
						<li key={h} className="flex gap-2 leading-relaxed">
							<span className="text-primary">•</span>
							<span>{h}</span>
						</li>
					))}
				</ul>
			)}

			{actionItems.length > 0 && (
				<div className="flex flex-wrap gap-1.5 pt-1">
					{actionItems.slice(0, 3).map((a) =>
						a.url ? (
							<Button
								key={a.label}
								asChild
								size="sm"
								variant="outline"
								className="h-7 text-xs"
							>
								<Link href={a.url}>{a.label}</Link>
							</Button>
						) : (
							<span
								key={a.label}
								className="rounded-full bg-primary/10 px-2 py-0.5 text-[11px] font-medium text-primary"
							>
								{a.label}
							</span>
						),
					)}
				</div>
			)}

			<div className="flex items-center justify-between gap-2 border-t border-border pt-2 text-[10px] text-muted-foreground">
				<span>Generated by {briefing.model}</span>
				{canRefresh && (
					<button
						type="button"
						onClick={handleRefresh}
						disabled={refreshing}
						className="flex items-center gap-1 hover:text-foreground transition-colors disabled:opacity-50"
					>
						<RefreshCw className={`size-3 ${refreshing ? "animate-spin" : ""}`} />
						Refresh
					</button>
				)}
			</div>
		</div>
	);
}
